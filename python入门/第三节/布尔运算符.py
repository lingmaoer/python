a, b = 1, 2
print("-------------------and 并且--------------")
print(a == 1 and b == 2)
print(a == 1 and b < 2)
print(a != 1 and b == 2)
print(a != 1 and b != 2)

print("-------------------or 或者--------------")
print(a == 1 or b == 2)
print(a == 1 or b < 2)
print(a != 1 or b == 2)
print(a != 1 or b != 2)

print("-------------------not 对布尔类型操作数取反--------------")
f=True
f2=False
print(not f)
print(not f2)

print("----------------in 与not in---------------")
s='helloworld'
print('w' in s)
print('k' in s)
print('w' not in s)
print('k' not in s)






































